package Bit_Manipulation.java;

public class _03_bitwiseOp {
    public static void main(String[] args) {
        System.out.println(5|9);
        System.out.println(5&9);
        System.out.println(5^9);
        System.out.println(5<<1);
        System.out.println(5<<2);
        System.out.println(5<<3);
        System.out.println(5>>1);
        System.out.println(5>>2);
        System.out.println(-5>>1);
        System.out.println(-5>>>1);
        System.out.println(5>>>1);

    }
}
